package it.itsar.firedb;

public class Task {
    String id;
    String name;
    Boolean status;

    public Task() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Boolean getStatus() {
        return status;
    }

    public void setStatus(Boolean status) {
        this.status = status;
    }

    @Override
    public String toString() {
        return getName()+" "+(getStatus() ? " fatto" : " non fatto");
    }
}